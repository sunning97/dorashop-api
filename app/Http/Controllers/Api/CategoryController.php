        $categories = Category::whereNull('parent_id')->get();

        $result = $this->getSubCategories($categories);
        
        return response()->json($result);
    }


    private function getSubCategories($categories){
        $tmp = [];
        foreach ($categories as $category) {
            if(Category::find($category->id)->subCategories->first()){
                $tmp2 = $this->getSubCategories(Category::find($category->id)->subCategories);
                $category = $category->toArray();
                $category["sub_categories"] = $tmp2;
                array_push($tmp,$category);
            } else {
                array_push($tmp,$category);
            }
        }

        return $tmp;
    }
